def get_temperature(weather):
    temperature = weather.temperature('celsius')["temp"]
    return temperature


def get_weather_description(weather):
    return weather.status


def get_humidity(weather):
    humidity = weather.humidity
    return humidity


def get_wind_speed(weather):
    wind_speed = weather.wind()["speed"]
    return wind_speed


def get_wind_direction(weather):
    wind_deg = weather.wind()["deg"]
    if 0 <= wind_deg < 45 or 315 <= wind_deg < 360:
        return "северный"
    elif 45 <= wind_deg < 135:
        return "восточный"
    elif 135 <= wind_deg < 225:
        return "южный"
    elif 225 <= wind_deg < 315:
        return "западный"
    else:
        return "нет данных"


