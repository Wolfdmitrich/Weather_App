@eel.expose
def get_weather_for_country(country):
    try:
        with open("config.json") as config_file:
            config = json.load(config_file)
            api_key = config["api_key"]

        owm = pyowm.OWM(api_key)
        mgr = owm.weather_manager()
        observation = mgr.weather_at_place(country)
        weather = observation.weather
        russian_weather = get_russian_weather_description(
            get_weather_description(observation.weather))

        return (
            russian_weather,
            get_temperature(weather),
            get_humidity(weather),
            get_wind_speed(weather),
            get_wind_direction(weather)
        )
    except pyowm.commons.exceptions.NotFoundError:
        return "ErrorNotFound"
    except Exception as err:
        print(f"Error: {err}")
        return "Error"


